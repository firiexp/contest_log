class CentipedeSocks {
    public:
    int fewestSocks(int C, int F, vector<int> sockCounts) {
        int cnt = 0, ans = 0;
        for (auto &&i : sockCounts) {
            int p = i/F;
            if(cnt + p >= C){
                return ans+F*(C-cnt);
            }
            ans += i;
            cnt += p;
        }
        return -1;
    }
};

