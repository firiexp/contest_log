using ll = long long;

class Make737Easy {
    public:
    int count(string S) {
        ll ans = 0;
        vector<int> v(S.length()+1, 0), v2(S.length()+1, 0);
        for (int i = 0; i < S.length()-1; ++i) {
            if(S[i] == '7') v[i+1] = v[i]+1;
            else v[i+1] = v[i];
        }
        for (int i = S.length()-1; i > 0; --i) {
            if(S[i] == '7') v2[i-1] = v2[i]+1;
            else v2[i-1] = v2[i];
        }
        for (int i = 1; i < S.length()-1; ++i) {
            if(S[i] == '3') ans += v[i]*v2[i];
        }
        return (int)ans;
    }
};

