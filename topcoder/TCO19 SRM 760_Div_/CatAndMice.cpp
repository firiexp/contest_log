class CatAndMice {
    public:

    int phi(int n) {
        if (n == 0) return 0;
        int ans = n;
        for (int i = 2; i*i <= n; i++) {
            if (n % i == 0) {
                ans -= ans / i;
                while (n % i == 0) n /= i;
            }
        }
        if (n > 1) ans -= ans / n;
        return ans;
    }


    long long countDirections(int N, int C) {
        ll ans = 0;
        for (int w = 1; w <= N; ++w) {
            if(N/w == C){
                ans += phi(w);
            }
        }
        return ans*8;
    }
};

